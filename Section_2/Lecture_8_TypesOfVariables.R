
# Lecture 8 - Types of Varialbles

# 1. Integer denoted by L - by default numbers entered in R are
#     assigned type Double.  Integer should only be used for
#     numbers that will not be arithmetically used, for instance
#     Keys or counts.

x <- 2L

# To check the type:

typeof(x)

# 2. Double

y <- 2.5

typeof(y)

# Complex data type

z <- 3 + 2i
typeof(z)

# Character

a <- 'h'
typeof(a)

# Logical

q <- TRUE
typeof(q)

q2 <- FALSE
typeof(q2)
